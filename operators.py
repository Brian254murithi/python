# Arithmetic Operators-preform simple calculations
x = 45
y = 21

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) # Modulus-Returns the reminder after dividing two numbers

# Comparison Operators
a = 56
b = 2

print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b) # equal to
print(a != b) # not equal to

# assignment operators-stores a value on the variable - left or right
z = 6
print(z)

num = 2
num += 10
print(num)

# logical operators
num1 = 5
num2 = 10
print(num1 < num2 and num1 == num2)
print(num1 < num2 or num1 == num2)
print(not(num1 < num2 and num1 == num2))






